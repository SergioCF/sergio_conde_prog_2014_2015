
public class UsarFor {

	public static void main(String[] args) {
	
	// Bucle manual... a lo guarro
		System.out.println("Numero 1");
		System.out.println("Numero 2");
		System.out.println("Numero 3");
	
	// Bucle for... como toca -> for(inicializacion;condicion"mientras";paso"QueHacemosEnCadaPasada"){
		for(int i=0;i<=10;i++){
			System.out.println("Bucle i= "+i);
		}


	}

}
