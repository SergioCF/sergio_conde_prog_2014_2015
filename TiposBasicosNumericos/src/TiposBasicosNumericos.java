
public class TiposBasicosNumericos {

	public static void main(String[] args) {
		int a,b=0;
		int c=13;
		byte numB=0;
		double numDb=5.6;
		float numF=2.3f;
		
		// Numeros enteros
		a=0;
		System.out.println(a);
		System.out.println("El valor de c=" +c);
		
		a++;
		System.out.println(a);
		a--;
		System.out.println(a);
		System.out.println(numB);
		
		// Numeros decimales
		System.out.println(numDb);
		numDb=numDb+2.3;
		System.out.println(numDb);
		System.out.println(numF);

	}

}
